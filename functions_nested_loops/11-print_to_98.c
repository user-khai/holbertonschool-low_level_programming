#include "main.h"
#include <stdio.h>

/**
 * print_to_98 - prints numbers ranging from n-98
 * @n: the start of the range
 * Description: numbers are separated by a comma and a space
 */
void print_to_98(int n)
{
	if (n <= 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	printf("\n");
}
